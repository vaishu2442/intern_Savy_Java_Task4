package java_task;

public class Task4 {

	
		 int id;
		 String name;
		 int age;
		 long mob;
		 
		public Task4(int id, String name, int age, long mob) {
			super();
			this.id = id;
			this.name = name;
			this.age = age;
			this.mob = mob;
		}
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public long getMob() {
			return mob;
		}
		public void setMob(long mob) {
			this.mob = mob;
		}
		 
		

	

}
